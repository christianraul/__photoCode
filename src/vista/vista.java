    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed

        String sql = "";

        sql = "INSERT INTO datosper (nombres,foto,ruta) VALUES (?,?,?)";
        try {
            PreparedStatement psd = cn.prepareStatement(sql);
            psd.setString(1, txtNombre.getText());

            FileInputStream archivoFoto;
            archivoFoto = new FileInputStream(txtRuta.getText());
            psd.setBinaryStream(2, archivoFoto);
            psd.setString(3, txtRuta.getText());
            int n = psd.executeUpdate();
            if (n > 0) {
                JOptionPane.showMessageDialog(null, "Registro Guardado");
            }
        } catch (SQLException ex) {
            Logger.getLogger(vista.class.getName()).log(Level.SEVERE, null, ex);
        } catch (FileNotFoundException ex) {
            Logger.getLogger(vista.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_btnGuardarActionPerformed

    private void jLabel2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel2MouseClicked
        JFileChooser archivo = new JFileChooser();
        int ventana = archivo.showOpenDialog(null);
        if (ventana == JFileChooser.APPROVE_OPTION) {
            File file = archivo.getSelectedFile();
            txtRuta.setText(String.valueOf(file));
            Image foto =getToolkit().getImage(txtRuta.getText());
            jLabel2.setIcon(new ImageIcon(foto));
        }
    }//GEN-LAST:event_jLabel2MouseClicked

